        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<BuysingooShop.Model.activity> DataTableToList(DataTable dt)
        {
            List<BuysingooShop.Model.activity> modelList = new List<BuysingooShop.Model.activity>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                BuysingooShop.Model.activity model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

